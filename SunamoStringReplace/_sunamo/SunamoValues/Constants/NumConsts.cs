namespace SunamoStringReplace._sunamo.SunamoValues.Constants;

/// <summary>
/// Provides commonly used numeric constants.
/// </summary>
internal class NumConsts
{
    /// <summary>
    /// Constant representing -1.
    /// </summary>
    internal const int MinusOne = -1;

    /// <summary>
    /// Default port number used when port cannot be parsed.
    /// </summary>
    internal const int DefaultPortIfCannotBeParsed = 587;

    /// <summary>
    /// Minimum age (18) due to GDPR - below 18 requires parent agreement.
    /// </summary>
    internal const int MinAge = 18;

    /// <summary>
    /// Number of bytes in a kilobyte.
    /// </summary>
    internal const long KiloByte = 1024;

    /// <summary>
    /// Zero as double.
    /// </summary>
    internal const double ZeroDouble = 0;

    /// <summary>
    /// Zero as float.
    /// </summary>
    internal const float ZeroFloat = 0;

    /// <summary>
    /// One as integer.
    /// </summary>
    internal const int One = 1;

    /// <summary>
    /// Zero as integer.
    /// </summary>
    internal const int ZeroInt = 0;

    /// <summary>
    /// Minimum DateTime value as short (10101).
    /// </summary>
    internal static short DateTimeMinValShort = 10101;

    /// <summary>
    /// Maximum DateTime value as short (32271).
    /// </summary>
    internal static short DateTimeMaxValShort = 32271;

    /// <summary>
    /// One thousand.
    /// </summary>
    internal static int OneThousand = 1000;
}
